def decode_lonlat(enLonLat):
    key_pos = int(''.join(map(lambda x: bin(ord(x))[-2:],
                              list(reversed(enLonLat))[:4]
                              )), 2)
    keys = KEYS[key_pos]

    fixed = 53 if keys[0] == 1 else 23
    nums = map(ord, enLonLat[:-4])

    for i in range(len(nums)):
        nums[i] -= fixed
        nums[i] -= keys[i + 1]

    lonlat = ''.join(map(chr, nums))
    return float(lonlat)

